<h3>Scope:</h3>

<p>
    <strong>PHP</strong>: the app is built within a Docker container with PHP, MySQL and Ngnix technologies. For organization I used roughed out MVC that I started previously and continued examping for this project.
</p>

<p>
    <strong>Frontend</strong>: the current frontend of this app uses very lite CSS and no JS as I focused on fleshing out the backend.
</p>

<p>
    <strong>MySQL</strong>: the app writes to a database using PDO and the SQL will load up when the docker is launched. The app should be using a OOP Singleton for the database connection.
</p>

<p>
    <strong>CRUD</strong>: The app can create, read, update and delete the todo items. I created a base CRUD object that other models can use to extend from. In this way the "todo" model can pass an array of variables to the parent CRUD model and it will creat all the necessary SQL and PDO commands to interact with the datbase in one place.
</p>

